package Day4Nisan10.TypeCasting;

public class Task2 {
//    Create 2 variables short & long | convert short to long | print long
//    Create 2 variables float & double | convert float to double | print double
//    Create 2 variables byte & float | convert byte to float | print float
public static void main(String[] args) {
    short distanceToThere = 1200;
    long convertLong = distanceToThere;
    System.out.println(convertLong);

    float valueOfHeat = 33.7F;
    double convertDouble = valueOfHeat;
    System.out.println(convertDouble);

    byte memoryOfDisk = 122;
    float convertFloat = memoryOfDisk;
    System.out.println(convertFloat);






}

}
