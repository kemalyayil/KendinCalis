package Day11Nisan20;

public class _04_IfStatementWithComparison {
    public static void main(String[] args) {

        int numberOfStudents = 25;
        if (numberOfStudents < 25){
            System.out.println("There are less than 25 students!");
        }
        if (numberOfStudents > 25){
            System.out.println("There are more than 25 students!");
        }
        if (numberOfStudents == 25){
            System.out.println("There are  25 students!");
        }
    }
}
