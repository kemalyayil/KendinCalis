package Day14Nisan26;

public class _01_ForLoops {
    public static void main(String[] args) {

        for (int i = 1; i <= 5; i++) {
            System.out.println("Hello World! " + i );
        }
        System.out.println(" ");

        for (int j = 5; j >= 1; j--) {
            System.out.println("Hello World? " + j );

        }
    }
}
