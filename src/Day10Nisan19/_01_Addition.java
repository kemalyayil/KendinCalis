package Day10Nisan19;

public class _01_Addition {
    public static void main(String[] args) {

        System.out.println(5+4);  // only values

        int x = 1;
        int y = 2;
        System.out.println(x +y); // variables
        
        int result = x + y;
        System.out.println("result = " + result);

        System.out.println(x+7); // variable and value
    }
}
