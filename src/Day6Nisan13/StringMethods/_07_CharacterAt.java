package Day6Nisan13.StringMethods;

public class _07_CharacterAt {

    public static void main(String[] args) {

        String welcome = "Hello TechnoStudy !"; // buarada amac yazilan numarada hangi karakter var, onu bulmak
        System.out.println(welcome.charAt(0)); // result is H

        char index0 = welcome.charAt(0); // diger yontem
        char index1 = welcome.charAt(1);
        char index2 = welcome.charAt(2);
        char index3 = welcome.charAt(3);
        System.out.println(index0);
        System.out.println(index1);
        System.out.println(index2);
        System.out.println(index3);

    }
}
