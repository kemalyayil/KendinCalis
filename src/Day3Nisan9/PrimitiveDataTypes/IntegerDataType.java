package Day3Nisan9.PrimitiveDataTypes;

public class IntegerDataType {

    public static void main(String[] args) {

        int youtubeViews = 200_000_000;
        System.out.println(youtubeViews);

        int myFirstInteger = 123456789;
        int mySecondIntegar = -98765432;
        System.out.println(myFirstInteger);
        System.out.println(mySecondIntegar);

        //System.out.println("Max value of short ="+ Short.MAX_VALUE);
        System.out.println("Max value of int =" + Integer.MAX_VALUE);
        System.out.println("Min value of int =" + Integer.MIN_VALUE);
    }
}
