package Day3Nisan9.PrimitiveDataTypes;

public class CharDataType {

    public static void main(String[] args) {

        char somethingFromKeyboard = '#';
        char letter = 'k';
        System.out.println(somethingFromKeyboard + "\n" + letter);

        // print backslash. \
        char escapeChar = '\'';
        System.out.println(escapeChar);



    }
}
